package day4;

public class ForLoops {
	public static void main(String[] args) {
		
	
	/* For loops provide us with mor nuanced control
	 * over the execution of the code in loop
	 * 
	 * For loops are comprised of 3 statements
	 * 	1- initialization of statement - statment that will execute one time
	 * 									prior to loop execution
	 * 2 - conditional statement - statement that will be evaluated prior to loop running
	 * 3 - increment/decrement of value - alter the value to bring closer to end of loop
	 */
	
		for (int i = 0; i< 10;i++) {
			System.out.println(i);
		}
		System.out.println("End of Application");
	}
}
